'use client';

import { useCartContext } from '../../context/CartContext';

interface CartIconProps {
  /** Custom class names */
  className?: string;
  /** Show badge even when count is 0 */
  showEmptyBadge?: boolean;
}

/**
 * Cart icon button with item count badge.
 * Clicking opens the cart drawer.
 */
export function CartIcon({ className = '', showEmptyBadge = false }: CartIconProps) {
  const { toggleCart, itemCount } = useCartContext();

  const showBadge = showEmptyBadge || itemCount > 0;

  return (
    <button
      type="button"
      onClick={toggleCart}
      className={`relative p-2 text-gray-600 hover:text-gray-900 focus:outline-none focus:ring-2 focus:ring-gray-500 rounded-lg ${className}`}
      aria-label={`Shopping cart with ${itemCount} items`}
    >
      <svg
        xmlns="http://www.w3.org/2000/svg"
        className="h-6 w-6"
        fill="none"
        viewBox="0 0 24 24"
        stroke="currentColor"
      >
        <path
          strokeLinecap="round"
          strokeLinejoin="round"
          strokeWidth={2}
          d="M3 3h2l.4 2M7 13h10l4-8H5.4M7 13L5.4 5M7 13l-2.293 2.293c-.63.63-.184 1.707.707 1.707H17m0 0a2 2 0 100 4 2 2 0 000-4zm-8 2a2 2 0 11-4 0 2 2 0 014 0z"
        />
      </svg>
      {showBadge && (
        <span className="absolute -top-1 -right-1 h-5 w-5 rounded-full bg-black text-white text-xs font-medium flex items-center justify-center">
          {itemCount > 99 ? '99+' : itemCount}
        </span>
      )}
    </button>
  );
}
